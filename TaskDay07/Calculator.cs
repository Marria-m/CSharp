using System;

namespace Day07
{
    internal class Calculator
    {
        // two integers
        public int Sum(int a, int b)
        {
            return a + b;
        }

        // three integers
        public int Sum(int a, int b, int c)
        {
            return a + b + c;
        }

        // two doubles
        public double Sum(double a, double b)
        {
            return a + b;
        }
    }
}
